import { API_BASE_URL, withAuthHeaders } from "./api";

export type TaskStatus = "OPEN" | "COMPLETED" | "CANCELLED";
export type TaskType =
  | "general"
  | "followup"
  | "call"
  | "email"
  | "whatsapp"
  | "meeting";

export interface Task {
  id: string;
  title: string;
  description?: string;
  ownerUserId: string;
  createdByUserId?: string;
  leadId?: string;
  orgId?: string;
  dueAt: string;
  status: TaskStatus;
  type: TaskType;
  isAutoGenerated: boolean;
  completedAt?: string;
  outcome?: string;
  completedByUserId?: string | { _id: string; name: string };
  popupState?: {
    lastShownAt?: string;
    dismissedAt?: string;
    workflowFollowupMissedEmittedAt?: string;
  };
  createdAt?: string;
  updatedAt?: string;
  // Populated fields
  ownerUser?: {
    id: string;
    name: string;
    email: string;
  };
  createdByUser?: {
    id: string;
    name: string;
    email: string;
  };
  lead?: {
    id: string;
    leadNumber: string;
    status: string;
  };
}

export interface TaskListQuery {
  ownerUserId?: string;
  leadId?: string;
  status?: TaskStatus;
  fromDue?: string;
  toDue?: string;
}

export interface CreateTaskPayload {
  title: string;
  description?: string;
  ownerUserId: string;
  leadId?: string;
  dueAt: string;
  type?: TaskType;
}

export interface UpdateTaskPayload {
  title?: string;
  description?: string;
  status?: TaskStatus;
  outcome?: string;
  completedAt?: string;
  completedByUserId?: string;
}

/**
 * List tasks with optional filters
 */
export const listTasks = async (query?: TaskListQuery): Promise<Task[]> => {
  const params = new URLSearchParams();
  if (query) {
    Object.entries(query).forEach(([key, value]) => {
      if (value) params.append(key, value);
    });
  }

  const url =
    `${API_BASE_URL}/tasks` +
    (params.toString() ? `?${params.toString()}` : "");

  const response = await fetch(url, {
    headers: withAuthHeaders(),
  });

  if (!response.ok) {
    let message = "Unable to fetch tasks";
    try {
      const data = await response.json();
      if (data?.message) message = data.message;
    } catch {
      // ignore
    }
    throw new Error(message);
  }

  const raw = (await response.json()) as any[];
  return mapTasks(raw);
};

/** List tasks for a specific lead (user must have lead access) */
export const listTasksForLead = (leadId: string): Promise<Task[]> =>
  listTasks({ leadId });

function mapTasks(raw: any[]): Task[] {
  return raw.map((t) => {
    const { _id, id, leadId, ...rest } = t;
    const task: Task = {
      id: id ?? _id,
      ...rest,
    };
    // Map populated leadId to lead field
    if (leadId && typeof leadId === "object" && leadId._id) {
      task.lead = {
        id: leadId._id || leadId.id,
        leadNumber: leadId.leadNumber || "",
        status: leadId.status || "",
      };
      task.leadId = leadId._id || leadId.id;
    } else if (leadId) {
      task.leadId = leadId;
    }
    return task;
  });
};

