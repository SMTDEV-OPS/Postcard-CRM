/**
 * Get today's follow-ups for the current user
 */
export const getTodaysFollowUps = async (): Promise<Task[]> => {
  const today = new Date();
  today.setHours(0, 0, 0, 0);
  const tomorrow = new Date(today);
  tomorrow.setDate(tomorrow.getDate() + 1);

  return listTasks({
    status: "OPEN",
    fromDue: today.toISOString(),
    toDue: tomorrow.toISOString(),
  });
};

/**
 * Get overdue follow-ups for the current user
 */
export const getOverdueFollowUps = async (): Promise<Task[]> => {
  const now = new Date();
  now.setHours(0, 0, 0, 0);

  const tasks = await listTasks({
    status: "OPEN",
    toDue: now.toISOString(),
  });

  // Filter to get only truly overdue (before today)
  return tasks.filter((task) => new Date(task.dueAt) < now);
};

/**
 * Get upcoming follow-ups for the current user (next 7 days)
 */
export const getUpcomingFollowUps = async (): Promise<Task[]> => {
  const today = new Date();
  today.setHours(0, 0, 0, 0);
  const weekFromNow = new Date(today);
  weekFromNow.setDate(weekFromNow.getDate() + 7);

  return listTasks({
    status: "OPEN",
    fromDue: today.toISOString(),
    toDue: weekFromNow.toISOString(),
  });
};

